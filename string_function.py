# STRING FUNCTION

Story = " A big brown fox jumps over the lazy dog"

print(len(Story))  # ---> (total numbers of characters)

print(Story.endswith("dog"))  # ---> (true if conditon is true else false)

print(Story.count("a"))  #---> (count the total numbers of occurance of any character of a given string)

print(Story.find("lazy"))    #---> (this function find the given string and return the index of first occurance of that word in the string)

print(Story.replace("fox","lion")) #---> (this function replace the old word with the new word in entire string)

